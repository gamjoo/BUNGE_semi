package info.db;

public class Boardfile {
	private int infa_num;
	private int inf_num;
	private String infa_filename;
	private String infa_servername;

	public int getInfa_num() {
		return infa_num;
	}

	public void setInfa_num(int infa_num) {
		this.infa_num = infa_num;
	}

	public int getInf_num() {
		return inf_num;
	}

	public void setInf_num(int inf_num) {
		this.inf_num = inf_num;
	}
	
	public String getInfa_filename() {
		return infa_filename;
	}

	public void setInfa_filename(String infa_filename) {
		this.infa_filename = infa_filename;
	}

	public String getInfa_servername() {
		return infa_servername;
	}

	public void setInfa_servername(String infa_servername) {
		this.infa_servername = infa_servername;
	}
}
