package infoboardlike.db;

public class InfoLike {
	private int no;
	private int inf_num;
	private String m_id;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getInf_num() {
		return inf_num;
	}
	public void setInf_num(int inf_num) {
		this.inf_num = inf_num;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
}
